export const getEventsByDay = (month: number, day: number): Event[] => {
  return events.filter(event => event.month === month && event.day === day);
};

export const getEventById = (id: string): Event | undefined => {
  return events.find(event => event.id === id);
};

export const getEventsByEra = (eraId: string): Event[] => {
  return events.filter(event => event.era.id === eraId);
};

export const getCrusadeEvents = (): Event[] => {
  return events.filter(event => 
    event.tags.some(tag => tag.toLowerCase().includes('crusade')) ||
    event.title.toLowerCase().includes('crusade')
  );
};

export const getEventsByTag = (tag: string): Event[] => {
  return events.filter(event => 
    event.tags.some(t => t.toLowerCase().includes(tag.toLowerCase()))
  );
};